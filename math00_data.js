// =========================
// Compatibility layer for math10.html / math11.html template
// - Exposes MATH_DATA in the expected format
// - Keeps original SET_BANK untouched
// =========================
(function(){
  if(!window.SET_BANK){
    console.error('SET_BANK not found.');
    return;
  }
  window.MATH_DATA = window.SET_BANK.map(function(set){
    return {
      id: set.id,
      title: set.title,
      questions: (set.prompts || []).map(function(p, idx){
        return {
          no: p,
          type: 'selection',
          answer: String((set.answers || [])[idx] ?? ''),
          choices: (set.choices || [])[idx] || ['1','2','3','4','5','6','7','모름']
        };
      })
    };
  });
})();
